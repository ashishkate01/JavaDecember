package Array;

public class NarrowingWidening {
    static void main(String[] args) {
        int a = 10;
        double b = a;
        System.out.println(a);
        System.out.println(b);

        double A = 10.5;
        int B = (int)A ;
        System.out.println(A);
        System.out.println(B);

    }
}
